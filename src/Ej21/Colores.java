package Ej21;

public class Colores {
    // Códigos ANSI para colores de texto
    public static final String RESET = "\u001B[0m";
    public static final String VERDE = "\u001B[32m";    // Éxito / Entrada
    public static final String ROJO = "\u001B[31m";     // Espera
    public static final String AZUL = "\u001B[34m";     // Salida
    public static final String AMARILLO = "\u001B[33m"; // Información
    public static final String CIAN = "\u001B[36m";     // Estado del parking
    public static final String PURPURA = "\u001B[35m";  // Estadísticas
}
